// 설계도, 붕어빵 틀, main()x
public class Book {

		// 1. 속성, 필드, 변수
		String title;	// 제목
		String author;// 저자
		// 2. 생성자 클래스 이름과 동일, 여러개 만듦, 리턴x
		public Book() {}
		public Book(String title) {
			this.title = title;
			author = "작가 미상";
		}
		public Book(String title, String author) {
			this.title = title;
			this.author = author;
		}
		// 3. method

	

}
